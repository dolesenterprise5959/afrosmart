// Install user-provided category placeholder images into AfroSmart.
//
// 1. Drop image files in  public/placeholders/_incoming/  named by either:
//      - an image key:   barber.webp, car-sedan.jpg, food-market.png …
//      - a category:     services.webp, cars.jpg, food.png, "real estate.webp" …
//    (any of .webp/.jpg/.jpeg/.png — next/image serves optimized WebP regardless)
// 2. Run:  node scripts/install-placeholders.mjs
//    → places files as public/placeholders/<key>.<ext> and rewrites the
//      PLACEHOLDER_FILES registry in lib/placeholders.ts automatically.
// 3. Rebuild / redeploy. User-uploaded photos always override these.

import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync, copyFileSync } from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const DIR = path.join(ROOT, "public", "placeholders");
const SRC = process.argv[2] ? path.resolve(process.argv[2]) : path.join(DIR, "_incoming");
const REG = path.join(ROOT, "lib", "placeholders.ts");
mkdirSync(SRC, { recursive: true });

const KEYS = new Set([
  "food-dishes", "food-vegetables", "food-market", "barber", "beauty-salon",
  "phone-repair", "carpenter", "motorbike-taxi", "taxi", "cargo-truck",
  "shop-storefront", "shop-market", "shop-retail", "home-modern", "apartment",
  "land", "car-sedan", "car-suv", "car-pickup", "smartphone", "electronics",
  "rental-home", "rental-equipment", "rental-vehicle", "jobs", "agriculture", "construction",
]);

// A single category image fans out to every key in that category.
const CATEGORY_KEYS = {
  food: ["food-dishes", "food-vegetables", "food-market"],
  services: ["barber", "beauty-salon", "phone-repair", "carpenter"],
  transport: ["motorbike-taxi", "taxi", "cargo-truck"],
  shops: ["shop-storefront", "shop-market", "shop-retail"],
  "real-estate": ["home-modern", "apartment", "land"],
  cars: ["car-sedan", "car-suv", "car-pickup"],
  phones: ["smartphone"],
  rentals: ["rental-home", "rental-equipment", "rental-vehicle"],
  jobs: ["jobs"],
  electronics: ["electronics"],
  beauty: ["beauty-salon"],
  construction: ["construction"],
  agriculture: ["agriculture"],
};
const ALIAS = { realestate: "real-estate", real_estate: "real-estate", "real-estate-": "real-estate" };
const norm = (s) => s.toLowerCase().replace(/\.[a-z0-9]+$/i, "").replace(/[\s_]+/g, "-").replace(/^\d+[-.]?/, "").trim();
const isImg = (f) => /\.(webp|jpe?g|png)$/i.test(f);

if (!existsSync(SRC)) { console.error(`Source dir not found: ${SRC}`); process.exit(1); }
const files = readdirSync(SRC).filter(isImg);
if (files.length === 0) {
  console.log(`No images found in ${SRC}. Drop files there (named by key or category) and re-run.`);
  process.exit(0);
}

let installed = 0;
for (const f of files) {
  const ext = path.extname(f).toLowerCase().replace("jpeg", "jpg");
  let base = norm(f);
  base = ALIAS[base] ?? base;
  let targets;
  if (KEYS.has(base)) targets = [base];
  else if (CATEGORY_KEYS[base]) targets = CATEGORY_KEYS[base];
  else { console.warn(`  ? skip "${f}" — name doesn't match a key or category`); continue; }
  for (const key of targets) {
    const dest = path.join(DIR, `${key}${ext}`);
    copyFileSync(path.join(SRC, f), dest);
    installed++;
  }
  console.log(`  ✓ ${f} → ${targets.join(", ")}`);
}

// Rebuild PLACEHOLDER_FILES from whatever <key>.<ext> files now exist.
const present = {};
for (const f of readdirSync(DIR)) {
  if (!isImg(f)) continue;
  const key = f.replace(/\.[a-z0-9]+$/i, "");
  if (KEYS.has(key)) present[key] = f;
}
const literal = "export const PLACEHOLDER_FILES: Record<string, string> = {\n" +
  Object.keys(present).sort().map((k) => `  "${k}": "${present[k]}",`).join("\n") +
  (Object.keys(present).length ? "\n" : "") + "};";
let reg = readFileSync(REG, "utf8");
reg = reg.replace(
  /\/\/ __PLACEHOLDER_FILES_START__[\s\S]*?\/\/ __PLACEHOLDER_FILES_END__/,
  `// __PLACEHOLDER_FILES_START__ (auto-generated by scripts/install-placeholders.mjs — do not edit by hand)\n${literal}\n// __PLACEHOLDER_FILES_END__`,
);
writeFileSync(REG, reg);

console.log(`\nInstalled ${installed} file(s). Active placeholder keys: ${Object.keys(present).length}`);
console.log(`Registry updated: lib/placeholders.ts. Next: npm run build && redeploy.`);
